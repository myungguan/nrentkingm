                        <h3>
                           <span><a href="#"><img src="<?=$config['img']?>/teaser/faqQicon.png" alt="Q">자차손해면책제도(CDW-Collision Damage Waiver)는 무엇인가요?</a> </span>
                        </h3>
                        <div class="answer4">
                            <strong> A.</strong> 
                            <span>
                                <strong>자차손해면책제도(CDW)</strong>란 손해보험사의 자차보험에 준하는 동일 기능을 말하며 렌트카 회사에서 자체적으로 대여기간별로 일정비용을 받고(옵션) 운전자의 차량손해를 면책해 주는 것을 말합니다.<br>즉, 렌트카 운전 중 차량 사고가 났을 경우 운전자의 수리비 부담을 덜어 드리기 위한 제도입니다.<br>

                                렌트킹의 자차손해면책제도는 각 지점마다 설정한 일반자차보험(면책금 30만원, 50만원, 100만원 등), 슈퍼자차보험(면책금 5만원, 10만원, 20만원 등), 완전자차보험(면책금 0원)이 있으며 적용 가능한 보험은 제휴 렌터카 회사별, 차량 별로 다를 수 있습니다.<br>

                                자차손해면책금은 사고 1건당 운전자가 최대로 부담하여야 하는 금액입니다. 예를 들어 면책금 300,000원의 자차보험에 가입된 차를 운전하는 경우, 사고 시 1,000,000원의 수리비가 나왔다고 하더라도 최대 300,000원까지만 납부를 하시고 나머지는 보험에서 처리하게 됩니다.<br>

                                <span>※ 유의 하셔야 할 부분은 보험처리 기준에 의거, 사고 1건당 면책금이 발생하며 2건 이상의 사고 발생 시 면책금은 사고 발생 횟수만큼 발생할 수 있습니다.</span>

